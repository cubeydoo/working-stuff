    /** Insertion sorts the provided data. */
    public static class InsertionSort implements SortingAlgorithm {
        @Override
        public void sort(int[] array, int k) {
            for (int i = 1; i < k; i++) {
                int current = array[i];
                while (i > 1 && current < array[i - 1]) {
                    array[i] = array[i - 1];
                    array[i - 1] = current;
                    i -= 1;
                }
            }
        }

        @Override
        public String toString() {
            return "Insertion Sort";
        }
    }

    /**
     * Selection Sort for small K should be more efficient
     * than for larger K. You do not need to use a heap,
     * though if you want an extra challenge, feel free to
     * implement a heap based selection sort (i.e. heapsort).
     */
    public static class SelectionSort implements SortingAlgorithm {
        @Override
        public void sort(int[] array, int k) {
            int lowIndex = 0;
            int curr = 0;
            for (int i = 0; i < k; i++) {
                for (int x = curr; x < k; x++) {
                    if (array[i] < array[lowIndex]) {
                        lowIndex = i;
                    }
                }
                int temp = array[lowIndex];
                array[lowIndex] = array[curr];
                array[curr] = temp;
                curr += 1;
                lowIndex = curr;
            }

        }

        @Override
        public String toString() {
            return "Selection Sort";
        }
    }

    /** Your mergesort implementation. An iterative merge
      * method is easier to write than a recursive merge method.
      * Note: I'm only talking about the merge operation here,
      * not the entire algorithm, which is easier to do recursively.
      */
    public static class MergeSort implements SortingAlgorithm {
        @Override
        public void sort(int[] array, int k) {
            int half = k / 2;
            int[] first = new int[half];
            int[] second = new int[k-half];

            for (int i = 0; i < half; i++) {
                first[i] = array[i];
            }
            for (int i = half; i < k; i++) {
                first[i - half] = array[i];
            }

            sort(first, half);
            sort(second, k-half);
            merge(first, second, half, k-half);
        }

        public void merge(int[] first, int[] second, int f, int s) {
            int i = 0;
            int x = 0;
            int finIndex = 0;
            int[] fin = new int[f+s];
            while (i < f && x < s) {
                if (first[i] <= second[x]) {
                    fin[finIndex] = first[i];
                    i++;
                } else {
                    fin[finIndex] = second[x];
                    x++;
                }
                finIndex++;
            }
            if (i < f) {
                while (i < f) {
                    fin[finIndex] = first[i];
                    finIndex++;
                    i++;
                }
            } else if (x < s) {
                while (x < s) {
                    fin[finIndex] = second[x];
                    finIndex++;
                    x++;
                }
            }
        }

        // may want to add additional methods

        @Override
        public String toString() {
            return "Merge Sort";
        }
    }

